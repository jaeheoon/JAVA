class CommentExample {
	// 자바 프로그램의 실행 진입점 역할의 메소드(엔트리 포인트)
	public static void main(String[] args) {
		// System.out.println("Hello World!");
		System.out.println("주석 테스트입니다. //는 주석으로 처리됩니다.");
		/* 커멘드 창에 텍스트를 출력하는 방법 3가지*/
		System.out.print("print는 출력 후 라인 바꾸지 않음");
		System.out.println("println은 출력 후 라인 바꿈");
		System.out.printf("%s", "printf는 %s라는 것을 출력, 출력 후 라인 바꾸지 않음");
		System.out.println("컨트롤+j 하면 줄 복사");
		System.out.print("알트+쉬프트+딜리트 하면 줄 삭제");

		
		/* println은 한줄을 다 쓰는 것, 
		print는 출력 후 라인 바꾸지 않음
		printf는 %s라는 것을 출력하고 라인 바꾸지 않음
		*/
	}
}
